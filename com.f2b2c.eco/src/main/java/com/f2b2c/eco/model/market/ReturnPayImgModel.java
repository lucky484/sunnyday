package com.f2b2c.eco.model.market;

public class ReturnPayImgModel {

	private int id;

	private int returnPayId;

	private String imgUrl;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getReturnPayId() {
		return returnPayId;
	}

	public void setReturnPayId(int returnPayId) {
		this.returnPayId = returnPayId;
	}

	public String getImgUrl() {
		return imgUrl;
	}

	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}

}
